#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t dstsize)
{
	size_t	i;
	size_t	srclen;
	char	*srccpy;

	srccpy = (char *)src;
	srclen = ft_strlen(srccpy);
	if (dstsize == 0)
		return (srclen);
	i = 0;
	while (src[i] && i < dstsize - 1)
	{
		dst[i] = srccpy[i];
		i++;
	}
	dst[i] = 0;
	return (srclen);
}
